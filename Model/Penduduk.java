package Model;

import java.util.Date;

public class Penduduk {
    private String nik;
    private String nama;
    private String tempatLahir;
    private Date tanggalLahir;
    private String jenisKelamin;
    private String golDarah;
    private String alamat;
    private String rtRw;
    private String kelDesa;
    private String kecamatan;
    private String agama;
    private String statusPerkawinan;
    private String pekerjaan;
    private String kewarganegaraan;
    private String foto;
    private String tandaTangan;
    private String berlakuHingga;
    private String kotaPembuatan;
    private Date tanggalPembuatan;

    public Penduduk() {}

    public String getNik() { return nik; }
    public void setNik(String nik) { this.nik = nik; }
    public String getNama() { return nama; }
    public void setNama(String nama) { this.nama = nama; }
    public String getTempatLahir() { return tempatLahir; }
    public void setTempatLahir(String tempatLahir) { this.tempatLahir = tempatLahir; }
    public Date getTanggalLahir() { return tanggalLahir; }
    public void setTanggalLahir(Date tanggalLahir) { this.tanggalLahir = tanggalLahir; }
    public String getJenisKelamin() { return jenisKelamin; }
    public void setJenisKelamin(String jenisKelamin) { this.jenisKelamin = jenisKelamin; }
    public String getGolDarah() { return golDarah; }
    public void setGolDarah(String golDarah) { this.golDarah = golDarah; }
    public String getAlamat() { return alamat; }
    public void setAlamat(String alamat) { this.alamat = alamat; }
    public String getRtRw() { return rtRw; }
    public void setRtRw(String rtRw) { this.rtRw = rtRw; }
    public String getKelDesa() { return kelDesa; }
    public void setKelDesa(String kelDesa) { this.kelDesa = kelDesa; }
    public String getKecamatan() { return kecamatan; }
    public void setKecamatan(String kecamatan) { this.kecamatan = kecamatan; }
    public String getAgama() { return agama; }
    public void setAgama(String agama) { this.agama = agama; }
    public String getStatusPerkawinan() { return statusPerkawinan; }
    public void setStatusPerkawinan(String statusPerkawinan) { this.statusPerkawinan = statusPerkawinan; }
    public String getPekerjaan() { return pekerjaan; }
    public void setPekerjaan(String pekerjaan) { this.pekerjaan = pekerjaan; }
    public String getKewarganegaraan() { return kewarganegaraan; }
    public void setKewarganegaraan(String kewarganegaraan) { this.kewarganegaraan = kewarganegaraan; }
    public String getFoto() { return foto; }
    public void setFoto(String foto) { this.foto = foto; }
    public String getTandaTangan() { return tandaTangan; }
    public void setTandaTangan(String tandaTangan) { this.tandaTangan = tandaTangan; }
    public String getBerlakuHingga() { return berlakuHingga; }
    public void setBerlakuHingga(String berlakuHingga) { this.berlakuHingga = berlakuHingga; }
    public String getKotaPembuatan() { return kotaPembuatan; }
    public void setKotaPembuatan(String kotaPembuatan) { this.kotaPembuatan = kotaPembuatan; }
    public Date getTanggalPembuatan() { return tanggalPembuatan; }
    public void setTanggalPembuatan(Date tanggalPembuatan) { this.tanggalPembuatan = tanggalPembuatan; }
}